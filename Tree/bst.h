#ifndef BST_H
#define BST_H

typedef int KeyType;

typedef struct BSTNode
{
	KeyType  			key ;    // 关键字
	struct BSTNode * 	lchild;  // 左孩子 
	struct BSTNode * 	rchild;  // 右孩子 
}BSTNode, *BSTree;

/*****************************************************************************
 *           向bst树中插入元素                                               *
 *****************************************************************************/ 
void InsertBST(BSTree * bst, KeyType key);

/*****************************************************************************
 *           创建BST树                                                       * 
 *****************************************************************************/ 
void CreateBST(BSTree * bst, int len, KeyType key[]);

/*****************************************************************************
 *           先序遍历BST树                                                   * 
 *****************************************************************************/
void PreOrderBST(BSTree bst);

/*****************************************************************************
 *           搜索BST树中的节点                                               * 
 *****************************************************************************/ 
BSTNode * SearchBST(BSTree bst, KeyType key);

/*****************************************************************************
 *           删除BST树中的节点                                               * 
 *****************************************************************************/
void DeleteBST(BSTree bst, KeyType key);

#endif // END BST_H
